package otr3

import "testing"

func Test_generateSMPSecretGeneratesASecret(t *testing.T) {
	aliceFingerprint := []byte{0x01, 0x02, 0x03, 0x04, 0x05, 0x06, 0x07, 0x08, 0x09, 0x0A, 0x0B, 0x0C, 0x0D, 0x0E, 0x0F, 0x10, 0x11, 0x12, 0x13, 0x14}
	bobFingerprint := []byte{0x31, 0x32, 0x33, 0x34, 0x35, 0x36, 0x37, 0x38, 0x39, 0x3A, 0x3B, 0x3C, 0x3D, 0x3E, 0x3F, 0x40, 0x41, 0x42, 0x43, 0x44}
	ssid := []byte{0xFF, 0xF1, 0xD1, 0xE4, 0x12, 0x34, 0x56, 0x68}
	secret := []byte("this is something secret")
	result := generateSMPSecret(aliceFingerprint, bobFingerprint, ssid, secret)
	assertDeepEquals(t, result, []byte{0xd9, 0xb2, 0xe5, 0x63, 0x21, 0xf9, 0xa9, 0xf8, 0xe3, 0x64, 0x60, 0x7c, 0x8c, 0x82, 0xde, 0xcd, 0x8e, 0x8e, 0x62, 0x9, 0xe2, 0xcb, 0x95, 0x2c, 0x7e, 0x64, 0x96, 0x20, 0xf5, 0x28, 0x6f, 0xe3})
}
